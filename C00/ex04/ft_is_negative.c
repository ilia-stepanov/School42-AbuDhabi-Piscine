#include <unistd.h>

void	ft_is_negative(int n)
{
	char	no;
	char	yes;

	no = 'N';
	yes = 'P';
	if (n < 0)
	{
		write(1, &no, 1);
	}
	else 
	{
		write(1, &yes, 1);
	}
}
