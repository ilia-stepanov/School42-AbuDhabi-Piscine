#ifndef FT_ABS_H
# define FT_ABS_H

# define ABS(Value)     \
({                      \
    int res;            \
    if (Value < 0) {    \
    res = -Value;       \
    } else {            \
    res = Value;        \
    }                   \
    res;                \
})
#endif